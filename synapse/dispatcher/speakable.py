"""speakable — детерминированный ФОРМАТНЫЙ фильтр «этот текст можно отдать в TTS как есть»
(KV-2, спека 2026-07-15 §4.2). Чистые regex, ноль сети, ноль I/O: pure/total — любой input
превращается в bool без исключения.

⚠️ Это НЕ фильтр безопасного содержания и НЕ semantic safety-классификатор (§4.2 запрещает
так его называть). Он не знает ничего о смысле текста: короткая разговорная фраза из
prompt-injection пройдёт его штатно. Безопасность держится на отдельном голосе Коры,
отсутствии записи этой речи в контекст диспетчера и неизменном approval/gate-контракте.

Вопрос, на который он отвечает, ровно один: «прозвучит ли это голосом как человеческая речь
или как зачитанный вслух markdown?». Цена ошибки асимметрична и задаёт смещение:
- false «грязный» (перебдели) = один лишний speakify-вызов / молчание в ленте — дёшево;
- false «чистый» (недобдели) = робот диктует в ухо `|---|---|` и /Users/... — дорого.
Поэтому все спорные случаи трактуются как грязные.
"""
from __future__ import annotations

import re

# --- разметка ---------------------------------------------------------------------------

# Код-фенсы (``` и ~~~) и инлайн-код (одиночный бэктик).
_FENCE = re.compile(r"(?:```|~~~|`)")
# `_` в прозе не встречается ни в русском, ни в английском: это либо snake_case-идентификатор
# (total_cost_usd), либо markdown-эмфаза (__bold__). Оба варианта — грязь.
_UNDERSCORE = re.compile(r"_")
# **bold** / *emphasis*-разметка. Одиночная `*` ловится буллет-правилом ниже, здесь — парная.
_EMPHASIS = re.compile(r"\*\*|\*\w|\w\*")
# Заголовок / буллет / нумерованный список / цитата — по разметке в НАЧАЛЕ строки.
# `[-*+]\s` требует пробела: «-5 градусов» и «*звёздочка» списком не считаются.
_BLOCK_MARKUP = re.compile(r"^[ \t]*(?:#{1,6}\s|[-*+]\s|\d+[.)]\s|>\s)", re.MULTILINE)
# Markdown-таблица: строка-разделитель (|---) ИЛИ строка, начинающаяся с трубы, ИЛИ строка
# с двумя+ трубами (ряд таблицы без разделителя тоже читается вслух как каша).
_TABLE = re.compile(r"^[ \t]*\|" r"|\|\s*:?-{2,}" r"|^[^\n|]*\|[^\n|]*\|", re.MULTILINE)

# --- пути / URL -------------------------------------------------------------------------

# Схема (https://, file://) или www-хост.
_URL = re.compile(r"[a-zA-Z][a-zA-Z0-9+.-]*://|\bwww\.", re.IGNORECASE)
# Абсолютный путь (/Users/x), домашний (~/Downloads), либо любой токен с 2+ слэшами
# (synapse/dispatcher/loop.py). Один слэш НЕ ловим намеренно: «и/или», «он/она» — речь.
_PATH = re.compile(
    r"(?<![^\s(\[])~?/[^\s/]"          # начало токена: /x или ~/x
    r"|[^\s/]+/[^\s/]+/[^\s/]"         # a/b/c — два слэша внутри токена
    r"|[A-Za-z]:\\"                    # C:\ (windows)
)
# Имя файла с расширением: стем начинается с латинской буквы и ≥2 символов, точка НЕ
# отделена пробелом, расширение 1-5 alnum. Ловит app.py, README.md, index.html.
# Стем-ограничения выключают ложняки: «0.97» (стем — цифра), «т.д.» (кириллица),
# «e.g.» (стем 1 символ), «готово. Теперь» (после точки пробел).
_FILENAME = re.compile(r"(?<![\w.])[A-Za-z][A-Za-z0-9-]+\.[A-Za-z0-9]{1,5}(?![\w.])")
# Ссылка «файл:строка» / «символ:строка» — app.py:1024, loop.py:88. Латинская буква в стеме
# обязательна, иначе правило съело бы время «10:30» и счёт «2:1».
_FILE_LINE = re.compile(r"(?<![\w.])[A-Za-z0-9-]*[A-Za-z][A-Za-z0-9.-]*:\d+")

# --- длинные идентификаторы -------------------------------------------------------------

# Граница «обычное слово vs идентификатор» проходит по ДВУМ осям сразу — длине И форме.
# Обычное слово любой длины — это буквы одного регистра-паттерна без цифр и без внутренней
# пунктуации («достопримечательность», «неопределённость»): такое слово голосом читается
# нормально и обязано проходить. Идентификатор — токен, который ЛОМАЕТ эту форму.
_IDENT_MIN_LEN = 12  # ниже порога живут M0, v1, KV-2, iOS, YouTube, MacBook, JavaScript
_CAMEL = re.compile(r"[a-z][A-Z]")
_HAS_LATIN = re.compile(r"[A-Za-z]")
_HAS_DIGIT = re.compile(r"\d")
_TOKEN_STRIP = " \t\r\n.,!?;:()[]{}«»\"'…—–"


def _is_long_identifier(token: str) -> bool:
    """Токен длиной ≥12, у которого форма не как у слова: camelCase-переход (getUserProfile),
    смесь латиницы с цифрами (1fe43e08, gemini-3.5-flash) или 2+ внутренних дефиса
    (naik-swapnali-joshi-think-ai). Даты (2026-07-15) остаются чистыми — в них нет букв."""
    if len(token) < _IDENT_MIN_LEN:
        return False
    if _CAMEL.search(token):
        return True
    if _HAS_LATIN.search(token) and _HAS_DIGIT.search(token):
        return True
    return token.count("-") >= 2 and bool(_HAS_LATIN.search(token))


_DIRTY_PATTERNS = (
    _FENCE, _UNDERSCORE, _EMPHASIS, _BLOCK_MARKUP, _TABLE,
    _URL, _PATH, _FILENAME, _FILE_LINE,
)


def speakable(text: str, *, max_chars: int) -> bool:
    """True, если текст можно отдать в TTS как есть, без speakify-переписывания.

    Pure/total (§11.1): ноль I/O, любой input → bool без исключения; пустая/whitespace-строка
    всегда False (озвучивать нечего). Кап длины проверяется ПЕРВЫМ — это и смысловое правило
    (длинная простыня не разговорная реплика), и защита от regex-прогона по мегабайту.
    """
    if not isinstance(text, str):
        return False
    stripped = text.strip()
    if not stripped:
        return False
    if len(stripped) > max_chars:
        return False
    for pattern in _DIRTY_PATTERNS:
        if pattern.search(stripped):
            return False
    return not any(_is_long_identifier(tok.strip(_TOKEN_STRIP)) for tok in stripped.split())
